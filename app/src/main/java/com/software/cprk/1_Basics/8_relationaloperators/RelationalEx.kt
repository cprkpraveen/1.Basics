package com.software.cprk.`1_Basics`.`8_relationaloperators`

fun main (args: Array<String>) {

    var num1 = 55
    var num2 = 55
    /*
        ---------------------------------------------------
            Relational Operators
        ---------------------------------------------------
     */
    //Equal To Operator
    println( "Equal To: "+(num1==num2)) // true

    // Not Equal To Operator
    println("Not Eqaual To: "+(num1!=num2)) // false

    // Greater Than Operator
    println("Greater Than: "+(num1>num2)) // false

    // Less Than Operator
    println("Less Than: "+(num1<num2)) // false

    // Greater Than Or Equal To Operator
    println("Greater Than Or Equal To: "+(num1>=num2)) // true

    // Less Than Or Equal To Operator
    println("Less Than OR Equal To : "+(num1<=num2)) // true
}